// Generates src/lib/devcenter/migrationManifest.generated.ts — the list of
// LOCAL migration files baked into the build, so the Platform Review's
// "DB & Migration Integrity" check can compare it, live, against what prod
// actually has applied (via the ops_migrations() reader). The serverless
// bundle has no supabase/migrations directory at runtime, hence build time.
//
// Runs on `prebuild` next to gen-codebase-context.mjs; safe to run by hand:
//   node scripts/gen-migration-manifest.mjs

import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const MIGRATIONS = join(ROOT, "supabase", "migrations");
const OUT = join(ROOT, "src", "lib", "devcenter", "migrationManifest.generated.ts");

// Local name -> name recorded in prod history. Mirrors NAME_ALIASES in
// scripts/check-migration-status.ts — keep the two in sync when adding aliases.
const NAME_ALIASES = {
  "0001_init": "arc_0001_init",
  "0002_rls": "arc_0002_rls",
  "0003_embeddings": "arc_0003_embeddings",
  arc_live_hardening: "arc_hardening",
  chemical_ghs_intelligence: "chemical_ghs_intelligence_bridge",
  chemical_concentration_hazard: "add_concentration_hazard_columns_to_chemical_inventory",
};

// Migrations applied by hand (no prod history record) and the schema object
// whose live presence proves they're applied. Mirrors SCHEMA_PROBE_FOR in
// scripts/check-migration-status.ts.
const SCHEMA_PROBES = {
  create_ai_telemetry: { kind: "table", table: "ai_telemetry" },
  ai_findings_rejection_reason: { kind: "column", table: "ai_findings", column: "rejection_reason" },
  chemical_container_capacity: { kind: "column", table: "chemical_inventory", column: "container_capacity" },
};

const entries = [];
for (const filename of readdirSync(MIGRATIONS).sort()) {
  if (!filename.endsWith(".sql")) continue;
  const match = filename.match(/^(\d{4,14})_(.+)\.sql$/);
  if (!match) continue; // DRAFT_* files are not migrations
  // ARC-era files use a 4-digit prefix (0001_init.sql) and are known on prod
  // by their full stem — same convention as check-migration-status.ts.
  const name = match[1].length === 4 ? filename.replace(/\.sql$/, "") : match[2];
  entries.push({ version: match[1], filename, name, prodName: NAME_ALIASES[name] ?? name });
}

const body = `// AUTO-GENERATED by scripts/gen-migration-manifest.mjs — do not edit.
// Local supabase/migrations manifest baked into the build so the Platform
// Review can diff it against prod's applied list at request time.

export interface LocalMigrationEntry {
  version: string;
  filename: string;
  name: string;
  /** Name this migration is recorded under in the prod history (alias-aware). */
  prodName: string;
}

export interface SchemaProbe {
  kind: "table" | "column";
  table: string;
  column?: string;
}

export const LOCAL_MIGRATIONS: LocalMigrationEntry[] = ${JSON.stringify(entries, null, 2)};

/** Hand-applied migrations verified by probing the live schema instead of the
 * migration history (they have no history record). */
export const SCHEMA_PROBES: Record<string, SchemaProbe> = ${JSON.stringify(SCHEMA_PROBES, null, 2)};
`;

writeFileSync(OUT, body, "utf-8");
console.log(`gen-migration-manifest: wrote ${entries.length} migrations to ${OUT}`);
